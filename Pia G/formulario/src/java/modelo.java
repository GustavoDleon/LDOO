public class modelo {
    private String nombre;
    private String primerA;
    private String segundoA;
    private String fecha;
    private String email;
    private String password;
    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the primerA
     */
    public String getPrimerA() {
        return primerA;
    }

    /**
     * @param primerA the primerA to set
     */
    public void setPrimerA(String primerA) {
        this.primerA = primerA;
    }

    /**
     * @return the segundoA
     */
    public String getSegundoA() {
        return segundoA;
    }

    /**
     * @param segundoA the segundoA to set
     */
    public void setSegundoA(String segundoA) {
        this.segundoA = segundoA;
    }

    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }
}
